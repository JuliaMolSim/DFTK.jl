# Build projection coefficients for a atoms array generated by term_nonlocal
# The ordering of the projector indices is (A,l,m,i), where A is running over all
# atoms, l, m are AM quantum numbers and i is running over all projectors for a
# given l. The matrix is block-diagonal with non-zeros only if A, l and m agree.
function build_projection_coefficients(T, psps, psp_positions)
    # TODO In the current version the proj_coeffs still has a lot of zeros.
    #      One could improve this by storing the blocks as a list or in a
    #      BlockDiagonal data structure
    n_proj = count_n_proj(psps, psp_positions)
    proj_coeffs = zeros(T, n_proj, n_proj)

    count = 0
    for (psp, positions) in zip(psps, psp_positions), _ in positions
        n_proj_psp = count_n_proj(psp)
        block = count+1:count+n_proj_psp
        proj_coeffs[block, block] = build_projection_coefficients(T, psp)
        count += n_proj_psp
    end
    @assert count == n_proj

    proj_coeffs
end

# Builds the projection coefficient matrix for a single atom
# The ordering of the projector indices is (l,m,i), where l, m are the
# AM quantum numbers and i is running over all projectors for a given l.
# The matrix is block-diagonal with non-zeros only if l and m agree.
function build_projection_coefficients(T::Type, psp::NormConservingPsp)
    n_proj = count_n_proj(psp)
    proj_coeffs = zeros(T, n_proj, n_proj)
    count = 0
    for l = 0:psp.lmax, _ = -l:l
        n_proj_l = count_n_proj_radial(psp, l)  # Number of i's
        range = count .+ (1:n_proj_l)
        proj_coeffs[range, range] = psp.h[l + 1]
        count += n_proj_l
    end
    proj_coeffs
end


@doc raw"""
Build projection vectors for a atoms array generated by term_nonlocal

```math
\begin{aligned}
H_{\rm at}  &= \sum_{ij} C_{ij} \ket{{\rm proj}_i} \bra{{\rm proj}_j} \\
H_{\rm per} &= \sum_R \sum_{ij} C_{ij} \ket{{\rm proj}_i(x-R)} \bra{{\rm proj}_j(x-R)}
\end{aligned}
```

```math
\begin{aligned}
\braket{e_k(G') \middle| H_{\rm per}}{e_k(G)}
        &= \ldots \\
        &= \frac{1}{ő©} \sum_{ij} C_{ij} \widehat{\rm proj}_i(k+G') \widehat{\rm proj}_j^*(k+G),
\end{aligned}
```

where ``\widehat{\rm proj}_i(p) = ‚ąę_{‚ĄĚ^3} {\rm proj}_i(r) e^{-ip¬∑r} dr``.

We store ``\frac{1}{\sqrt ő©} \widehat{\rm proj}_i(k+G)`` in `proj_vectors`.
"""
function build_projection_vectors(basis::PlaneWaveBasis{T}, kpt::Kpoint,
                                  psps::AbstractVector{<: NormConservingPsp},
                                  psp_positions) where {T}
    unit_cell_volume = basis.model.unit_cell_volume
    n_proj = count_n_proj(psps, psp_positions)
    n_G    = length(G_vectors(basis, kpt))
    G_plus_k = Gplusk_vectors(basis, kpt)
    proj_vectors = zeros_like(G_plus_k, Complex{eltype(psp_positions[1][1])}, n_G, n_proj)

    # Compute the columns of proj_vectors = 1/‚ąöő© \hat proj_i(k+G)
    # Since the proj_i are translates of each others, \hat proj_i(k+G) decouples as
    # \hat proj_i(p) = ‚ąę proj(r-R) e^{-ip¬∑r} dr = e^{-ip¬∑R} \hat proj(p).
    # The first term is the structure factor, the second the form factor.
    offset = 0  # offset into proj_vectors
    for (psp, positions) in zip(psps, psp_positions)
        # Compute position-independent form factors
        G_plus_k_cart = Gplusk_vectors_cart(basis, kpt)
        form_factors = build_projector_form_factors(psp, G_plus_k_cart)

        # Combine with structure factors
        for r in positions
            # k+G in this formula can also be G, this only changes an unimportant phase factor
            structure_factors = map(p -> cis2pi(-dot(p, r)), G_plus_k)
            @views for iproj = 1:count_n_proj(psp)
                proj_vectors[:, offset+iproj] .=
                    structure_factors .* form_factors[:, iproj] ./ sqrt(unit_cell_volume)
            end
            offset += count_n_proj(psp)
        end
    end
    @assert offset == n_proj

    proj_vectors
end

"""
Build form factors (Fourier transforms of projectors) for all orbitals of an atom centered at 0.
This is a highly optimized, GPU compatible function.
"""
function build_projector_form_factors(psp::NormConservingPsp,
                                      G_plus_k::AbstractVector{Vec3{T}}) where {T}
    arch = architecture(G_plus_k)
    ps = norm.(G_plus_k)

    n_proj = count_n_proj(psp)
    form_factors = similar(G_plus_k, Complex{T}, length(G_plus_k), n_proj)
    G_indices = to_device(arch, collect(1:length(G_plus_k)))
    for l = 0:psp.lmax
        n_proj_l = count_n_proj_radial(psp, l)
        offset = sum(x -> count_n_proj(psp, x), 0:l-1; init=0) .+
                 n_proj_l .* (collect(1:2l+1) .- 1) # offset about m for a given l
        for i = 1:n_proj_l
            # Performs same computation as build_form_factors(eval_psp_projector_fourier, l, [G_plus_k]),
            # but in a highly optimized, vectorized, and GPU compatible way.
            proj_li = eval_psp_projector_fourier(psp, i, l, ps)
            for m = -l:l
                map!(@view(form_factors[:, offset[m + l + 1] + i]), G_indices) do iG
                    angular = (-im)^l * solid_harmonic_real(l, m, G_plus_k[iG])
                    proj_li[iG] * angular
                end
            end
        end
    end
    form_factors
end

"""
Build Fourier transform factors of an atomic function centered at 0 for a given l.
The function should return a Hankel transform or equivalent,
with a division by p^l already included.
"""
function build_form_factors(fun::Function, l::Int,
                            G_plus_ks::AbstractVector{<:AbstractVector{Vec3{TT}}}) where {TT}
    # TODO this function can be generally useful, should refactor to a separate file eventually
    T = real(TT)

    form_factors = Vector{Matrix{Complex{T}}}(undef, length(G_plus_ks))
    for (ik, G_plus_k) in enumerate(G_plus_ks)
        form_factors_ik = Matrix{Complex{T}}(undef, length(G_plus_k), 2l + 1)
        for (ip, p) in enumerate(G_plus_k)
            radials_p = fun(norm(p))
            for m = -l:l
                # see "Fourier transforms of centered functions" in the docs for the formula
                angular = (-im)^l * solid_harmonic_real(l, m, p)
                form_factors_ik[ip, m+l+1] = radials_p * angular
            end
        end
        form_factors[ik] = form_factors_ik
    end

    form_factors
end

# Helpers for phonon computations.
function build_projection_coefficients(basis::PlaneWaveBasis{T}, psp_groups) where {T}
    psps          = [basis.model.atoms[first(group)].psp for group in psp_groups]
    psp_positions = [basis.model.positions[group] for group in psp_groups]
    build_projection_coefficients(T, psps, psp_positions)
end
function build_projection_vectors(basis::PlaneWaveBasis, kpt::Kpoint,
                                  psp_groups::AbstractVector{<: AbstractVector{<: Int}},
                                  positions)
    psps          = [basis.model.atoms[first(group)].psp for group in psp_groups]
    psp_positions = [positions[group] for group in psp_groups]
    build_projection_vectors(basis, kpt, psps, psp_positions)
end
function PDPŌąk(basis, positions, psp_groups, kpt, kpt_minus_q, Ōąk)
    D = build_projection_coefficients(basis, psp_groups)
    P = build_projection_vectors(basis, kpt, psp_groups, positions)
    P_minus_q = build_projection_vectors(basis, kpt_minus_q, psp_groups, positions)
    P * (D * P_minus_q' * Ōąk)
end

function compute_dynmat_őīH(::TermAtomicNonlocal, basis::PlaneWaveBasis{T}, Ōą, occupation,
                           őīŌą, őīoccupation, q) where {T}
    S = complex(T)
    model = basis.model
    psp_groups = [group for group in model.atom_groups
                  if model.atoms[first(group)] isa ElementPsp]

    # Early return if no pseudopotential atoms.
    isempty(psp_groups) && return nothing

    őīforces = [zero(Vec3{S}) for _ = 1:length(model.positions)]
    for group in psp_groups
        őīŌą_plus_q = transfer_blochwave_equivalent_to_actual(basis, őīŌą, q)
        for (ik, kpt) in enumerate(basis.kpoints)
            Ōąk = Ōą[ik]
            őīŌąk_plus_q = őīŌą_plus_q[ik].Ōąk
            kpt_plus_q = őīŌą_plus_q[ik].kpt

            for idx in group
                őīforces[idx] += map(1:3) do őĪ
                    őīHŌąk = derivative_wrt_őĪs(model.positions, őĪ, idx) do positions_őĪs
                        PDPŌąk(basis, positions_őĪs, psp_groups, kpt_plus_q, kpt, Ōą[ik])
                    end
                    őīHŌąk_plus_q = derivative_wrt_őĪs(model.positions, őĪ, idx) do positions_őĪs
                        PDPŌąk(basis, positions_őĪs, psp_groups, kpt, kpt, Ōą[ik])
                    end
                    -basis.kweights[ik] * sum(2occupation[ik] .* vec(columnwise_dots(őīŌąk_plus_q, őīHŌąk)) +
                                              őīoccupation[ik] .* 2vec(real(columnwise_dots(Ōąk, őīHŌąk_plus_q))))
                end
            end
        end
    end

    mpi_sum!(őīforces, basis.comm_kpts)
end

@views function compute_dynmat(term::TermAtomicNonlocal, basis::PlaneWaveBasis{T}, Ōą,
                               occupation; őīŌąs, őīoccupations, q=zero(Vec3{T}),
                               kwargs...) where {T}
    S = complex(T)
    model = basis.model
    positions = model.positions
    n_atoms = length(positions)
    n_dim = model.n_dim

    # Two contributions: dynmat_őīH and dynmat_őī¬≤H.

    # dynmat_őīH
    dynmat_őīH = zeros(S, 3, n_atoms, 3, n_atoms)
    for s = 1:n_atoms, őĪ = 1:n_dim
        dynmat_őīH[:, :, őĪ, s] .-= stack(
            compute_dynmat_őīH(term, basis, Ōą, occupation, őīŌąs[őĪ, s], őīoccupations[őĪ, s], q)
        )
    end

    psp_groups = [group for group in model.atom_groups
                  if model.atoms[first(group)] isa ElementPsp]
    # Early return if no pseudopotential atoms.
    isempty(psp_groups) && return dynmat_őīH

    # dynmat_őī¬≤H
    dynmat_őī¬≤H = zeros(S, 3, n_atoms, 3, n_atoms)
    őī¬≤HŌą = zero.(Ōą)
    for s = 1:n_atoms, őĪ = 1:n_dim, ő≤ = 1:n_dim  # zero if s ‚Č† t
        for (ik, kpt) in enumerate(basis.kpoints)
            őī¬≤HŌą[ik] = derivative_wrt_őĪs(basis.model.positions, ő≤, s) do positions_ő≤s
                derivative_wrt_őĪs(positions_ő≤s, őĪ, s) do positions_ő≤sőĪs
                    PDPŌąk(basis, positions_ő≤sőĪs, psp_groups, kpt, kpt, Ōą[ik])
                end
            end
            dynmat_őī¬≤H[ő≤, s, őĪ, s] += sum(occupation[ik][n] * basis.kweights[ik] *
                                              dot(Ōą[ik][:, n], őī¬≤HŌą[ik][:, n])
                                          for n=1:size(Ōą[ik], 2))
        end
    end

    dynmat_őīH + dynmat_őī¬≤H
end

# őīH is the Fourier transform perturbation of the nonlocal potential due to a position
# displacement e^{iq¬∑r} of the őĪ coordinate of atom s.
function compute_őīHŌą_őĪs(::TermAtomicNonlocal, basis::PlaneWaveBasis{T}, Ōą, őĪ, s, q) where {T}
    model = basis.model
    psp_groups = [group for group in model.atom_groups
                  if model.atoms[first(group)] isa ElementPsp]

    Ōą_minus_q = transfer_blochwave_equivalent_to_actual(basis, Ōą, -q)
    map(enumerate(basis.kpoints)) do (ik, kpt)
        derivative_wrt_őĪs(model.positions, őĪ, s) do positions_őĪs
            PDPŌąk(basis, positions_őĪs, psp_groups, kpt, Ōą_minus_q[ik].kpt, Ōą_minus_q[ik].Ōąk)
        end
    end
end
